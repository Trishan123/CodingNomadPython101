# Fahrenheit to Celsius:
# ----------------------
# Write the necessary code to convert a degree in Fahrenheit
# to Celsius and print it to the console. Use variable names!

# Assign the Fahrenheit temperature to a variable
fahrenheit = 100  # You can change this value to any Fahrenheit temperature

# Convert Fahrenheit to Celsius using the formula
celsius = (fahrenheit - 32) * 5 / 9

# Print the result to the console
print(f"{fahrenheit} degrees Fahrenheit is equal to {celsius} degrees Celsius.")
